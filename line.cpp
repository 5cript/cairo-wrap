#include "line.hpp"

namespace Cairo
{
//#####################################################################################################################
    Line::Line(DrawContext* ctx, double x, double y, double x2, double y2)
        : Shape{ctx, x, y}
        , x2_{x2}
        , y2_{y2}
    {

    }
//---------------------------------------------------------------------------------------------------------------------
    void Line::draw(Pen const& line, Pen const& fill)
    {
        cairo_move_to(*ctx_, x_, y_);
        cairo_line_to(*ctx_, x2_, y2_);
        stroke(ctx_, line);
    }
//#####################################################################################################################
}
